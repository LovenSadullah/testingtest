while True:

    x = int(input("enter your first number:"))
    y = int(input("enter your second number:")) # input the um 1st & 2nd
    z = input("what do you want to do with these two numbers?input from -, +, / or *")

    if z == ("-"):   
        print(x - y)

    if z == ("+") :  
        print(x + y)
        
    if z == ("*") :  
        print(x * y)
        
    if z == ("/") :  
        print(x / y)
        
    break
        

        

